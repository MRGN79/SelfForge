const DEFAULT_LANG = 'es'

export function getLang() {
  const stored = localStorage.getItem('selfforge_lang')
  if (stored && translations[stored]) return stored
  const browser = navigator.language?.slice(0, 2)
  return translations[browser] ? browser : DEFAULT_LANG
}

export function setLang(lang) {
  localStorage.setItem('selfforge_lang', lang)
}

export function t(key, vars = {}) {
  const lang = getLang()
  const str = translations[lang]?.[key] ?? translations[DEFAULT_LANG]?.[key] ?? key
  return Object.entries(vars).reduce((s, [k, v]) => s.replace(`{${k}}`, v), str)
}

